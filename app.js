function renderFaq() {
  const grid = $("[data-faq-grid]");
  if (!grid) return;
  grid.innerHTML = "";

  translations[lang].faq.forEach((item, index) => {
    const article = document.createElement("article");
    article.className = "faq-item reveal";

    const button = document.createElement("button");
    button.type = "button";
    button.className = "faq-question";
    button.setAttribute("aria-expanded", index === 0 ? "true" : "false");
    button.innerHTML = `<span>${item.q}</span><svg viewBox="0 0 24 24" aria-hidden="true"><path d="M12 5v14M5 12h14"></path></svg>`;

    const answer = document.createElement("div");
    answer.className = "faq-answer";
    answer.textContent = item.a;

    // Перший пункт одразу відкритий (без анімації при ініціалізації)
    if (index === 0) {
      article.classList.add("open");
      answer.style.height      = 'auto';
      answer.style.opacity     = '1';
      answer.style.paddingBottom = '24px';
      answer.style.overflow    = 'hidden';
    }

    button.addEventListener("click", () => {
      const wasOpen = article.classList.contains("open");

      // Закриваємо всі відкриті
      $$(".faq-item", grid).forEach((faq) => {
        if (faq.classList.contains("open")) {
          faq.classList.remove("open");
          $(".faq-question", faq).setAttribute("aria-expanded", "false");
          faqClose($(".faq-answer", faq));
        }
      });

      // Відкриваємо натиснутий (якщо він не був відкритий)
      if (!wasOpen) {
        article.classList.add("open");
        button.setAttribute("aria-expanded", "true");
        faqOpen(answer);
      }
    });

    article.append(button, answer);
    grid.append(article);
  });

  observeReveals();
}

