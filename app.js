let revealObserver;
function observeReveals() {
  if (!revealObserver) {
    revealObserver = new IntersectionObserver(
      (entries) => {
        entries.forEach((entry) => {
          if (entry.isIntersecting) {
            entry.target.classList.add("visible");
            revealObserver.unobserve(entry.target);
          }
        });
      },
      { threshold: 0.12, rootMargin: "0px 0px -40px 0px" }
    );
  }

  $$(".reveal:not(.visible)").forEach((node) => revealObserver.observe(node));
}

function setupCustomSelects() {
  $$('[data-custom-select]').forEach(wrapper => {
    const btn    = wrapper.querySelector('.custom-select-btn');
    const valEl  = wrapper.querySelector('.custom-select-val');
    const list   = wrapper.querySelector('.custom-select-list');
    const hidden = wrapper.querySelector('input[type="hidden"]');
    const items  = $$('li[role="option"]', wrapper);
    if (!btn || !list || !hidden) return;

    // ── Portal: move list to <body> so it escapes every stacking context ──
    document.body.appendChild(list);
    list.style.position = 'fixed';

    const reposition = () => {
      const r = btn.getBoundingClientRect();
      list.style.top   = (r.bottom + 6) + 'px';
      list.style.left  = r.left + 'px';
      list.style.width = r.width + 'px';
    };

    const close = () => {
      list.classList.remove('is-open');
      wrapper.classList.remove('open');
      btn.setAttribute('aria-expanded', 'false');
    };

    const open = () => {
      reposition();
      list.classList.add('is-open');
      wrapper.classList.add('open');
      btn.setAttribute('aria-expanded', 'true');
    };

    btn.addEventListener('click', (e) => {
      e.stopPropagation();
      list.classList.contains('is-open') ? close() : open();
    });

    items.forEach(item => {
      item.addEventListener('click', () => {
        items.forEach(i => i.removeAttribute('aria-selected'));
        item.setAttribute('aria-selected', 'true');
        hidden.value = item.dataset.value;
        valEl.textContent = item.textContent.trim();
        wrapper.classList.add('has-value');
        close();
      });
    });

    document.addEventListener('click',  (e) => {
      if (!wrapper.contains(e.target) && !list.contains(e.target)) close();
    });
    document.addEventListener('keydown', (e) => { if (e.key === 'Escape') close(); });
    window.addEventListener('scroll',   () => { if (list.classList.contains('is-open')) reposition(); }, { passive: true });
    window.addEventListener('resize',   () => { if (list.classList.contains('is-open')) reposition(); }, { passive: true });
  });
}

