function setupContactForm() {
  // ── Formsubmit.co — безкоштовно, безліміт, реєстрація не потрібна ────────
  // Перший раз після відправки прийде лист на пошту для активації — підтвердіть його
  const FORM_EMAIL = "aureon.enterprise.main@gmail.com";
  // ─────────────────────────────────────────────────────────────────────────

  const form = $("[data-contact-form]");
  if (!form) return;

  const submitBtn = form.querySelector('[type="submit"]');
  const btnLabel = submitBtn ? submitBtn.querySelector('span') || submitBtn : null;
  let originalLabel = btnLabel ? btnLabel.textContent : "Send Message";

  // Inject feedback element
  let feedback = form.querySelector('.form-feedback');
  if (!feedback) {
    feedback = document.createElement('div');
    feedback.className = 'form-feedback';
    form.appendChild(feedback);
  }

  form.addEventListener("submit", async (event) => {
    event.preventDefault();

    // Validate custom select
    const serviceInput = form.querySelector('input[name="service"]');
    if (serviceInput && !serviceInput.value) {
      const wrapper = form.querySelector('[data-custom-select]');
      const btn = wrapper?.querySelector('.custom-select-btn');
      if (btn) {
        btn.focus();
        wrapper.classList.add('select-error');
        setTimeout(() => wrapper.classList.remove('select-error'), 800);
      }
      return;
    }

    // Loading state
    if (submitBtn) {
      submitBtn.disabled = true;
      originalLabel = btnLabel ? btnLabel.textContent : submitBtn.textContent;
      if (btnLabel) btnLabel.textContent = "Sending…";
      else submitBtn.textContent = "Sending…";
    }
    feedback.className = 'form-feedback';
    feedback.textContent = '';

    try {
      const data = new FormData(form);
      const payload = {
        name:    data.get("name")    || "",
        contact: data.get("contact") || "",
        service: data.get("service") || "",
        message: data.get("message") || ""
      };

      const response = await fetch(`https://formsubmit.co/ajax/${FORM_EMAIL}`, {
        method: "POST",
        headers: { "Content-Type": "application/json", "Accept": "application/json" },
        body: JSON.stringify(payload)
      });

      const result = await response.json();

      if (result.success === "true" || result.success === true) {
        feedback.className = 'form-feedback success';
        feedback.textContent = "✓ Message sent! We'll reply within hours.";
        form.reset();
        // Reset custom select display
        const valEl = form.querySelector('.custom-select-val');
        const wrapper = form.querySelector('[data-custom-select]');
        if (valEl) {
          const lang = document.documentElement.lang || 'en';
          valEl.textContent = translations[lang]?.form_service_ph || "Choose an option…";
        }
        if (wrapper) wrapper.classList.remove('has-value');
      } else {
        throw new Error("Server error");
      }
    } catch {
      feedback.className = 'form-feedback error';
      feedback.textContent = "Something went wrong. Please try again or contact us directly.";
    } finally {
      if (submitBtn) {
        submitBtn.disabled = false;
        if (btnLabel) btnLabel.textContent = originalLabel;
        else submitBtn.textContent = originalLabel;
      }
    }
  });
}

function prefersReducedMotion() {
  return window.matchMedia("(prefers-reduced-motion: reduce)").matches;
}

function setupCursor() {
  if (!window.matchMedia("(pointer: fine)").matches || prefersReducedMotion()) return;

  const dot = $("[data-cursor-dot]");
  const halo = $("[data-cursor-halo]");
  if (!dot || !halo) return;

  document.body.classList.add("cursor-enabled");

  // Event-driven: RAF only fires when cursor moves, not in an infinite loop
  let raf = 0;
  let x = 0;
  let y = 0;
  let lastMx = -1;
  let lastMy = -1;
  const W = () => window.innerWidth;
  const H = () => window.innerHeight;

  const applyPosition = () => {
    const tx = x.toFixed(1);
    const ty = y.toFixed(1);
    dot.style.transform  = `translate3d(${tx}px,${ty}px,0) translate(-50%,-50%)`;
    halo.style.transform = `translate3d(${tx}px,${ty}px,0) translate(-50%,-50%)`;

    // Throttle CSS var updates — they trigger full style recalc on the whole doc.
    // Only update when position shifted by ≥1% of viewport.
    const mx = (x / W()) * 100;
    const my = (y / H()) * 100;
    if (Math.abs(mx - lastMx) > 1 || Math.abs(my - lastMy) > 1) {
      document.documentElement.style.setProperty("--mx", mx.toFixed(1) + "%");
      document.documentElement.style.setProperty("--my", my.toFixed(1) + "%");
      lastMx = mx;
      lastMy = my;
    }
    raf = 0;
  };

  const move = (event) => {
    x = event.clientX;
    y = event.clientY;
    document.body.classList.add("cursor-ready");
    // Schedule one RAF per mousemove batch — no continuous loop
    if (!raf) raf = requestAnimationFrame(applyPosition);
  };

  window.addEventListener("mousemove", move, { passive: true });
  window.addEventListener("mouseleave", () => document.body.classList.remove("cursor-ready"));
  window.addEventListener("mouseenter", () => document.body.classList.add("cursor-ready"));

  const INTERACTIVE = "a, button, input, select, textarea, .service-card, .why-card, .step-card, .payout-card, .offer-board";

  document.addEventListener("mouseover", (event) => {
    if (event.target.closest(INTERACTIVE)) document.body.classList.add("cursor-active");
  });

  document.addEventListener("mouseout", (event) => {
    if (event.target.closest(INTERACTIVE)) document.body.classList.remove("cursor-active");
  });
}

