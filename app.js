const $ = (selector, root = document) => root.querySelector(selector);
const $$ = (selector, root = document) => Array.from(root.querySelectorAll(selector));

let lang = localStorage.getItem("aureon_lang") || "en";

function tr(key) {
  return translations[lang]?.[key] ?? translations.en[key] ?? key;
}

function applyLanguage(nextLang) {
  lang = translations[nextLang] ? nextLang : "en";
  localStorage.setItem("aureon_lang", lang);
  document.documentElement.lang = lang === "uk" ? "uk" : lang;
  document.title = tr("title");

  const description = $('meta[name="description"]');
  if (description) description.setAttribute("content", tr("description"));

  $$("[data-i18n]").forEach((node) => {
    const key = node.dataset.i18n;
    if (translations[lang][key] !== undefined) node.textContent = tr(key);
  });

  $$("[data-placeholder]").forEach((node) => {
    node.setAttribute("placeholder", tr(node.dataset.placeholder));
  });

  $("[data-current-flag]").textContent = tr("flag");
  $("[data-current-label]").textContent = tr("label");

  $$("[data-lang]").forEach((button) => {
    button.classList.toggle("active", button.dataset.lang === lang);
  });

  renderFaq();
  syncCustomSelectsLang();
}

/* ---- FAQ helpers -------------------------------------------------- */

function faqOpen(answer) {
  answer.getAnimations().forEach(a => a.cancel());

  // scrollHeight returns full content height even with height:0 + overflow:hidden —
  // no need to set height:auto first (avoids the brief flash of unstyled content)
  const targetH = answer.scrollHeight;

  const anim = answer.animate(
    [
      { height: '0px',          opacity: '0', paddingBottom: '0px'  },
      { height: targetH + 'px', opacity: '1', paddingBottom: '24px' }
    ],
    { duration: 420, easing: 'cubic-bezier(0.16, 1, 0.3, 1)', fill: 'forwards' }
  );

  // After animation: release to auto so content can reflow on window resize
  anim.onfinish = () => {
    answer.style.height        = 'auto';
    answer.style.opacity       = '1';
    answer.style.paddingBottom = '24px';
    answer.style.overflow      = 'hidden';
  };
}

function faqClose(answer) {
  answer.getAnimations().forEach(a => a.cancel());

  // Фіксуємо поточну висоту перед колапсом
  const startH = answer.scrollHeight;
  answer.style.height = startH + 'px';

  answer.animate(
    [
      { height: startH + 'px', opacity: '1', paddingBottom: '24px' },
      { height: '0px',         opacity: '0', paddingBottom: '0px'  }
    ],
    { duration: 320, easing: 'cubic-bezier(0.4, 0, 0.2, 1)', fill: 'forwards' }
  );
}

