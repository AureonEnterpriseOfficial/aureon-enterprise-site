/* =============================================
   PARTICLE CANVAS BACKGROUND
   ============================================= */

function setupParticleCanvas() {
  if (prefersReducedMotion()) return;

  const canvas = document.getElementById('particle-canvas');
  if (!canvas) return;

  const ctx = canvas.getContext('2d', { alpha: true });
  let W, H, particles, animId;

  // Зменшено COUNT 55→28 і MAX_DIST 130→80 → ~85% менше обчислень на кадр
  const COUNT    = 28;
  const MAX_DIST = 80;
  const MAX_DIST_SQ = MAX_DIST * MAX_DIST; // уникаємо sqrt — порівнюємо квадрати
  const COLORS = [
    'rgba(93, 125, 255,',
    'rgba(139, 92, 246,',
    'rgba(40, 209, 146,',
    'rgba(215, 174, 98,'
  ];

  // Обмежуємо FPS до 30 — для плавного фону 30 FPS достатньо
  const TARGET_MS = 1000 / 30;
  let lastTime = 0;

  function resize() {
    W = canvas.width  = window.innerWidth;
    H = canvas.height = window.innerHeight;
  }

  function createParticle() {
    const color = COLORS[Math.floor(Math.random() * COLORS.length)];
    return {
      x: Math.random() * W,
      y: Math.random() * H,
      vx: (Math.random() - 0.5) * 0.3,
      vy: (Math.random() - 0.5) * 0.3,
      r: Math.random() * 1.6 + 0.5,
      alpha: Math.random() * 0.45 + 0.15,
      color,
    };
  }

  function init() {
    resize();
    particles = Array.from({ length: COUNT }, createParticle);
  }

  function tick(now) {
    animId = requestAnimationFrame(tick);

    // Пропускаємо кадр якщо ще не пройшло TARGET_MS
    if (now - lastTime < TARGET_MS) return;
    lastTime = now;

    ctx.clearRect(0, 0, W, H);

    for (let i = 0; i < COUNT; i++) {
      const p = particles[i];
      p.x += p.vx;
      p.y += p.vy;

      if (p.x < -10) p.x = W + 10;
      else if (p.x > W + 10) p.x = -10;
      if (p.y < -10) p.y = H + 10;
      else if (p.y > H + 10) p.y = -10;

      // Малюємо крапку
      ctx.beginPath();
      ctx.arc(p.x, p.y, p.r, 0, Math.PI * 2);
      ctx.fillStyle = p.color + p.alpha + ')';
      ctx.fill();

      // З'єднуємо лініями — порівнюємо квадрат відстані (без sqrt)
      for (let j = i + 1; j < COUNT; j++) {
        const q = particles[j];
        const dx = p.x - q.x;
        const dy = p.y - q.y;
        const distSq = dx * dx + dy * dy;

        if (distSq < MAX_DIST_SQ) {
          // Quadratic falloff — avoids Math.sqrt entirely
          const lineAlpha = (1 - distSq / MAX_DIST_SQ) * 0.14;
          ctx.beginPath();
          ctx.moveTo(p.x, p.y);
          ctx.lineTo(q.x, q.y);
          ctx.strokeStyle = p.color + lineAlpha + ')';
          ctx.lineWidth = 0.6;
          ctx.stroke();
        }
      }
    }
  }

  let resizeTimer;
  window.addEventListener('resize', () => {
    clearTimeout(resizeTimer);
    resizeTimer = setTimeout(() => { resize(); }, 200);
  }, { passive: true });

  init();
  animId = requestAnimationFrame(tick);
}

/* =============================================
   ACTIVE NAV LINK TRACKING
   ============================================= */

function setupActiveNav() {
  const navLinks = $$('.desktop-nav a');
  if (!navLinks.length) return;

  const sectionIds = navLinks.map(a => a.getAttribute('href').replace('#', '')).filter(Boolean);
  const sections   = sectionIds.map(id => document.getElementById(id)).filter(Boolean);
  if (!sections.length) return;

  const setActive = (href) => {
    navLinks.forEach(link =>
      link.classList.toggle('nav-active', link.getAttribute('href') === href)
    );
  };

  // ── Immediate update on click ──────────────────────────────────────────
  // IntersectionObserver fires asynchronously (can be 300-1000ms late).
  // Clicking a link should switch the highlight right away.
  let clickLockTimer = 0;

  navLinks.forEach(link => {
    link.addEventListener('click', () => {
      setActive(link.getAttribute('href'));
      // Lock the scroll-observer from overriding for 1.4s
      // (enough time for smooth-scroll to settle).
      clearTimeout(clickLockTimer);
      clickLockTimer = setTimeout(() => { clickLockTimer = 0; }, 1400);
    });
  });

  // ── Scroll-based update (only while not click-locked) ─────────────────
  const observer = new IntersectionObserver(
    (entries) => {
      if (clickLockTimer) return; // user just clicked — don't fight it
      entries.forEach(entry => {
        if (entry.isIntersecting) {
          setActive('#' + entry.target.id);
        }
      });
    },
    { threshold: 0.25, rootMargin: '-80px 0px -35% 0px' }
  );

  sections.forEach(s => observer.observe(s));
}

